public class file5 {
    public static void main(String[] args) {
        int num = 1;
        switch (num){
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                break;
            case 3:
                System.out.println("three");
                break;
            default:
                System.out.println("不適切な値です。");
        }
    }
}
